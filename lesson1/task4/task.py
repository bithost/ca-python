#Create a Python list with at least five elements. Perform operations like adding an element, removing an element, and accessing an element at a specific index.

# Create a list with at least five elements
my_list = [1, 2, 3, 4, 5]

# Add an element to the list
my_list.append(6)
print("After adding an element:", my_list)

# Remove an element from the list
my_list.remove(3)
print("After removing an element:", my_list)

# Access an element at a specific index
second_element = my_list[2]
print("Element at index 2:", second_element)
